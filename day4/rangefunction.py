# range(): values between the rane
print(range(10))

# get values with in range
print(list(range(10)))

#last=n-1; start=5
print(list(range(5,10)))

# get values with in range with increment-odd numbers
print(list(range(1,10,2)))

# get values with in range with increment-even numbers
print(list(range(0,10,2)))

# range with decrement ; starting =10; last=1, exclude last number
print(list(range(10,1,-1)))

#always exclude last number; last=n-1
print(list(range(-10,-5)))

print(list(range(-10,-5,2)))
